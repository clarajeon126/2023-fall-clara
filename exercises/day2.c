#include <stdio.h>

int main(void) {
    int value1=1;
    int value2=2;
    printf("enter 2 integers: ");
    scanf("%d", &value1);
    scanf("%d", &value2);
    int sum = value1 + value2;
    int diff = value1 - value2;
    int product = value1 * value2;
    int div = value1 / value2;
    int mod = value1 % value2;
    printf("%d + %d = %d\n", value1, value2, sum);
    printf("%d - %d = %d\n", value1, value2, diff);
    printf("%d * %d = %d\n", value1, value2, product);
    printf("%d / %d = %d\n", value1, value2, div);
    printf("%d %% %d = %d\n", value1, value2, mod);
    return 0;
}
